document.addEventListener('DOMContentLoaded', () => {
    const langSwitch = document.getElementById('langSwitch');
    const form = document.getElementById('iceForm');
    const successMsg = document.getElementById('successMsg');
    
    // 1. Detect language
    const browserLang = (navigator.language || navigator.userLanguage).split('-')[0];
    let currentLang = localStorage.getItem('iceLang') || (dictionary[browserLang] ? browserLang : 'es');
    
    // Set initial value
    langSwitch.value = currentLang;
    applyLanguage(currentLang);

    // 2. Handle change
    langSwitch.addEventListener('change', (e) => {
        currentLang = e.target.value;
        localStorage.setItem('iceLang', currentLang);
        applyLanguage(currentLang);
    });

    function applyLanguage(lang) {
        const texts = dictionary[lang];
        if (!texts) return;

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (texts[key]) el.textContent = texts[key];
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (texts[key]) el.placeholder = texts[key];
        });
    }

    // 3. Handle Form Submit
    form.addEventListener('submit', async (e) => {
        e.preventDefault();
        const email = document.getElementById('emailInput').value;
        const button = form.querySelector('button');
        const originalText = button.textContent;
        button.textContent = "...";
        button.disabled = true;
        
        try {
            // Apuntamos al proxy inverso en el subdominio dedicado
            const apiUrl = 'https://api.deft.work/api/subscribe';
                
            const response = await fetch(apiUrl, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ email, lang: currentLang })
            });
            
            if (response.ok) {
                form.style.display = 'none';
                successMsg.style.display = 'block';
            } else {
                alert(dictionary[currentLang].error);
                button.textContent = originalText;
                button.disabled = false;
            }
        } catch (error) {
            console.error(error);
            alert(dictionary[currentLang].error);
            button.textContent = originalText;
            button.disabled = false;
        }
    });
});
